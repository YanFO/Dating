def build_memory_context(memory: dict) -> str:
    """將 MatchMemory profile 格式化為 prompt context 區塊，注入 system prompt"""
    sections = []

    # 1. 基本資訊與重要節日
    basics = []
    if memory.get("birthday"):
        basics.append(f"- 生日：{memory['birthday']}")
    if memory.get("mbti_or_zodiac"):
        basics.append(f"- 星座/MBTI：{memory['mbti_or_zodiac']}")
    for a in memory.get("anniversaries", []):
        basics.append(f"- 紀念日：{a.get('label', '')}（{a.get('date', '')}）")
    for r in memory.get("routine", []):
        basics.append(f"- 作息：{r}")
    if basics:
        sections.append("### 基本資訊與重要節日\n" + "\n".join(basics))

    # 2. 飲食偏好
    diet = []
    for item in memory.get("favorite_food", []):
        diet.append(f"- 喜歡的食物：{item}")
    for item in memory.get("favorite_restaurant", []):
        diet.append(f"- 愛店：{item}")
    for item in memory.get("disliked_food", []):
        diet.append(f"- 討厭的食物：{item}")
    for item in memory.get("dietary_restrictions", []):
        diet.append(f"- 飲食禁忌：{item}")
    for item in memory.get("beverage_customization", []):
        diet.append(f"- 飲料偏好：{item}")
    if diet:
        sections.append("### 飲食偏好\n" + "\n".join(diet))

    # 3. 地點與休閒娛樂
    leisure = []
    for item in memory.get("favorite_places", []):
        leisure.append(f"- 喜歡的地方：{item}")
    for item in memory.get("travel_wishlist", []):
        leisure.append(f"- 想去的地方：{item}")
    for item in memory.get("hobbies", []):
        leisure.append(f"- 嗜好：{item}")
    for item in memory.get("entertainment_tastes", []):
        leisure.append(f"- 影視/音樂品味：{item}")
    if leisure:
        sections.append("### 地點與休閒娛樂\n" + "\n".join(leisure))

    # 4. 情感地雷與情緒撫慰
    emotional = []
    for item in memory.get("landmines", []):
        emotional.append(f"- 地雷（避免提及）：{item}")
    for item in memory.get("pet_peeves", []):
        emotional.append(f"- 小煩躁：{item}")
    for item in memory.get("soothing_methods", []):
        emotional.append(f"- 安撫方式：{item}")
    for item in memory.get("love_languages", []):
        emotional.append(f"- 愛之語：{item}")
    if emotional:
        sections.append("### 情感地雷與情緒撫慰\n" + "\n".join(emotional))

    # 5. 物質與送禮
    gifting = []
    for item in memory.get("wishlist", []):
        gifting.append(f"- 想要的東西：{item}")
    for item in memory.get("favorite_brands", []):
        gifting.append(f"- 偏好品牌：{item}")
    for item in memory.get("aesthetic_preference", []):
        gifting.append(f"- 審美風格：{item}")
    if gifting:
        sections.append("### 物質與送禮偏好\n" + "\n".join(gifting))

    # 6. 其他
    others = memory.get("other_notes", [])
    if others:
        sections.append("### 其他備註\n" + "\n".join(f"- {n}" for n in others))

    if not sections:
        return ""

    header = "\n## 關於對方的記憶（請在回覆建議中適當運用這些資訊，例如串連記憶、個人化邀約等）："
    return header + "\n\n" + "\n\n".join(sections)
